package com.rabe7ne.util;

public class CharInterval {

	private char from;
	
	private char to;
	
	public CharInterval(char from, char to) {
		this.from = from;
		this.to = to;
	}

	public char getFrom() {
		return from;
	}

	public void setFrom(char from) {
		this.from = from;
	}

	public char getTo() {
		return to;
	}

	public void setTo(char to) {
		this.to = to;
	}
	
}
